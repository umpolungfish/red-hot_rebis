# ── ALL_FG_NAMES ──
ALL_FG_NAMES = sorted(SMARTS_PATTERNS.keys())


def detect_functional_groups(smiles):
    """Detect ALL functional groups in a molecule by SMILES.
    
    Uses RDKit SMARTS matching against all ~200+ patterns.
    
    Args:
        smiles: SMILES string of the molecule
        
    Returns:
        List of (fg_name, count) tuples for detected functional groups
    """
    if not HAS_RDKIT:
        return [("no_rdkit", 0)]
    
    try:
        mol = Chem.MolFromSmiles(smiles)
        if mol is None:
            return [("invalid_smiles", 0)]
    except Exception:
        return [("invalid_smiles", 0)]
    
    results = []
    for name, pat in COMPILED_SMARTS.items():
        try:
            matches = mol.GetSubstructMatches(pat)
            if matches:
                results.append((name, len(matches)))
        except Exception:
            pass
    
    return sorted(results, key=lambda x: -x[1])


def integrate_into_compiler(compiler_module):
    """Patch a ch3mpiler compiler module with our exhaustive FG database.
    
    Replaces FG table and adds SMARTS-based detection.
    """
    # Extend FG table
    if hasattr(compiler_module, 'FG'):
        compiler_module.FG.update(FG_TUPLES)
    
    # Add FG_TOKENS for name-based matching
    if hasattr(compiler_module, 'FG_TOKENS'):
        for fg_name in ALL_FG_NAMES:
            # Add token: if FG name contains underscore, add parts
            parts = fg_name.replace('_', ' ')
            compiler_module.FG_TOKENS[fg_name] = fg_name
    
    # Add detection function
    compiler_module.detect_fgs = detect_functional_groups
    
    return True


