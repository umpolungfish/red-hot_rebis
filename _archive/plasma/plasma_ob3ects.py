# OB3ECT COLLECTION & ACCESSORS
# ═══════════════════════════════════════════════════════════════════

PLASMA_OB3ECTS: List[PlasmaOb3ect] = [
    PRIMITIVE_FIRST_OB3ECT,
    HIGH_ENERGY_OB3ECT,
    ROCK_CRACKING_OB3ECT,
    IDEAL_DEVICE_OB3ECT,
]

def plasma_ob3ect_by_name(name: str) -> Optional[PlasmaOb3ect]:
    """Look up a plasma ob3ect by name (case-insensitive partial match)."""
    name_lower = name.lower()
    for ob in PLASMA_OB3ECTS:
        if name_lower in ob.name.lower():
            return ob
    return None

def plasma_ob3ect_opcode_map(name: str) -> Dict[str, str]:
    """Get the opcode→physics mapping for a plasma ob3ect."""
    ob = plasma_ob3ect_by_name(name)
    if ob is None:
        return {}
    return ob.opcode_map

def plasma_ob3ect_bootstrap(name: str) -> List[Dict]:
    """Get the bootstrap lane assignments for a plasma ob3ect."""
    ob = plasma_ob3ect_by_name(name)
    if ob is None:
        return []
    return ob.bootstrap_lanes

def plasma_ob3ect_tuple(name: str) -> Dict[str, str]:
    """Get the 12-primitive tuple for a plasma ob3ect."""
    ob = plasma_ob3ect_by_name(name)
    if ob is None:
        return {}
    return ob.tuple_glyphs

def plasma_ob3ect_imas_ops(name: str) -> List[str]:
    """Get the IMASM opcode sequence for a plasma ob3ect."""
    ob = plasma_ob3ect_by_name(name)
    if ob is None:
        return []
    return ob.imas_ops

def plasma_ob3ect_frobenius(name: str) -> Dict:
    """Get the Frobenius split/fuse pair for a plasma ob3ect."""
    ob = plasma_ob3ect_by_name(name)
    if ob is None:
        return {}
    return {
        "split": ob.split_element,
        "outputs": ob.split_outputs,
        "fuse": ob.fuse_element,
        "verdict": ob.frobenius_verdict,
    }

def plasma_ob3ect_registers(name: str) -> Dict[str, str]:
    """Get the Belnap four-valued registers for a plasma ob3ect."""
    ob = plasma_ob3ect_by_name(name)
    if ob is None:
        return {}
    return {
        "00_void": ob.register_void,
        "01_true": ob.register_true,
        "10_false": ob.register_false,
        "11_both": ob.register_both,
    }

def all_plasma_opcode_maps() -> Dict[str, Dict[str, str]]:
    """Return all opcode maps keyed by ob3ect name."""
    return {ob.name: ob.opcode_map for ob in PLASMA_OB3ECTS}

def dialetheia_complete_ob3ects() -> List[str]:
    """List plasma ob3ects with dialetheia_complete=True (can hold contradictions)."""
    return [ob.name for ob in PLASMA_OB3ECTS if ob.dialetheia_complete]


