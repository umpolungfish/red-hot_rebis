@dataclass
class PlasmaOb3ect:
    """A self-imscribing plasma ob3ect with full IMASM operational semantics."""
    name: str
    domain: str
    # 12-primitive tuple
    tuple_glyphs: Dict[str, str]
    # IMASM arrangement
    imas_ops: List[str]           # ordered opcode sequence
    imas_sig: Tuple[int,int,int,int]  # (token_div, self_ref, frob_order, period_class)
    imas_period: int
    dialetheia_complete: bool
    # Opcode → physics mapping
    opcode_map: Dict[str, str]
    # Frobenius pair
    split_element: str
    split_outputs: List[str]
    fuse_element: str
    frobenius_verdict: str
    # Belnap registers (void, true, false, both)
    register_void: str
    register_true: str
    register_false: str
    register_both: str
    # Bootstrap lane assignments
    bootstrap_lanes: List[Dict]
    # exOS mapping
    exos_compiler: str
    exos_ipc: str
    exos_memory: str
    exos_scheduler: str
    exos_alfs: str
    # Verification
    closure_verified: bool
    tier: str
    c_score: Optional[float] = None


